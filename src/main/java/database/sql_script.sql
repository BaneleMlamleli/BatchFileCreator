CREATE TABLE industry_types (
	id INTEGER PRIMARY KEY,
	name TEXT,
	enabled INTEGER,
	natural_person_enabled INTEGER,
	legal_entity_enabled INTEGER,
	code TEXT
);

CREATE TABLE countries (
    id INTEGER PRIMARY KEY,
    name TEXT NOT NULL,
    code TEXT NOT NULL,
    display_order INTEGER NOT NULL,
    enabled INTEGER NOT NULL,
    risk_profile_id INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE transaction_reasons (
	id INTEGER PRIMARY KEY,
	description TEXT NOT NULL,
	enabled BOOLEAN NOT NULL DEFAULT TRUE,
	code TEXT,
	direction BOOLEAN
);

CREATE TABLE business_relationship_types (
	id INTEGER PRIMARY KEY,
	name TEXT NOT NULL,
	enabled BOOLEAN NOT NULL DEFAULT false,
	natural_person_enabled BOOLEAN NOT NULL DEFAULT false,
	legal_entity_enabled BOOLEAN NOT NULL DEFAULT false,
	code TEXT
);

-- NOTE: This table is no longer required as I'm using a dropdown box to list keep and list all the options
